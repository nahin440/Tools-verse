export const toolBySlug = new Map(TOOLS.map((t) => [t.slug, t]));

export function getTool(slug) {
  return toolBySlug.get(slug) ?? null;
}

export function getToolsByCategory(category) {
  return TOOLS.filter((t) => t.category === category);
}

export function getAllToolSlugs() {
  return TOOLS.map((t) => t.slug);
}

export function getCategoryBySlug(categorySlug) {
  const entry = Object.entries(CATEGORIES).find(([, v]) => v.slug === categorySlug);
  return entry ? { key: entry[0], ...entry[1] } : null;
}

export function getRelatedTools(slug, limit = 4) {
  const tool = getTool(slug);
  if (!tool) return [];
  return TOOLS.filter((t) => t.category === tool.category && t.slug !== slug).slice(0, limit);
}
